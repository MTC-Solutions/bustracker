    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function profile()
    {
        $user = Auth::user();

        if(!$user->hasRole("ROLE_DRIVER") && !$user->hasRole("ROLE_PASSENGER")){
            return redirect()->back()->with("error", "Access denied");
        }

        if($user->hasRole("ROLE_DRIVER")){
            $driver = Driver::where("email", $user->email)->first();
            return view("users.profile", ["driver"=>$driver]);
        }

        $passenger = Passenger::where("email", $user->email)->first();
        return view("users.profile", ["passenger"=>$passenger]);
    }

